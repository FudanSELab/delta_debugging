)

var knownTypes = map[string]struct {
	object     IstioObject
	collection IstioObjectList
}{
EOF

CRDS="ServiceClass ServicePlan"

for crd in $CRDS; do
cat << EOF
	config.$crd.Type: {
		object: &${crd}{
			TypeMeta: meta_v1.TypeMeta{
				Kind:       "${crd}",
				APIVersion: config.IstioAPIVersion,
			},
		},
		collection: &${crd}List{},
	},
EOF

done

TEST="FakeConfig"

for crd in $TEST; do
cat << EOF
	mock.$crd.Type: {
		object: &${crd}{
			TypeMeta: meta_v1.TypeMeta{
				Kind:       "${crd}",
				APIVersion: config.IstioAPIVersion,
			},
		},
		collection: &${crd}List{},
	},
EOF

done

